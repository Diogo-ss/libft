#include "libft.h"

char    *ft_memmove(char *dst, const char *src, size_t len)
{
    size_t count;

    count = 0;
    while (count < len) 
    { 
        dst[count] = src[count];
        
        if (src[count] == '\0')
            break;

        count++;
    }
    return dst;
}
